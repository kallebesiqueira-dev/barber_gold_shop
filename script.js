function setMinimumBookingDate() {
  if (!dateInput) {
    return;
  }

  const today = new Date();
  const localDate = new Date(today.getTime() - today.getTimezoneOffset() * 60000)
    .toISOString()
    .split("T")[0];
  dateInput.min = localDate;
}

function applyTranslations(language) {
  const content = translations[language];
  if (!content) {
    return;
  }

  document.documentElement.lang = language;
  document.title = language === "it" ? "Gold Barber Shop" : "Gold Barber Shop";

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (!content[key]) {
      return;
    }
    element.textContent = content[key];
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    if (!content[key]) {
      return;
    }
    element.setAttribute("placeholder", content[key]);
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    const key = element.dataset.i18nAriaLabel;
    if (!content[key]) {
      return;
    }
    element.setAttribute("aria-label", content[key]);
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    const key = element.dataset.i18nAlt;
    if (!content[key]) {
      return;
    }
    element.setAttribute("alt", content[key]);
  });

  document.querySelectorAll("[data-i18n-title]").forEach((element) => {
    const key = element.dataset.i18nTitle;
    if (!content[key]) {
      return;
    }
    element.setAttribute("title", content[key]);
  });

  const serviceSelect = document.querySelector("#booking-service");
  if (serviceSelect) {
    Array.from(serviceSelect.options).forEach((option) => {
      const key = option.dataset.i18n;
      if (key && content[key]) {
        option.textContent = content[key];
      }
    });
  }

  langButtons.forEach((button) => {
    const isActive = button.dataset.lang === language;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-checked", String(isActive));
  });

  const activeCode = document.querySelector(".active-lang-code");
  if (activeCode) {
    activeCode.textContent = language.toUpperCase();
  }

  document.querySelectorAll("[data-active-flag]").forEach((flag) => {
    flag.classList.toggle("is-hidden", flag.dataset.activeFlag !== language);
  });

  currentLanguage = language;
  window.localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
}

function getCookieBannerMarkup(content) {
  return `
    <div class="cookie-banner-inner">
      <div class="cookie-copy">
        <strong>${content.cookieTitle}</strong>
        <p>${content.cookieText} <a href="cookies.html">${content.legalCookies}</a>.</p>
      </div>
      <div class="cookie-actions">
        <button class="cookie-btn cookie-btn-secondary" type="button" data-cookie-action="reject">${content.cookieReject}</button>
        <button class="cookie-btn cookie-btn-primary" type="button" data-cookie-action="accept">${content.cookieAccept}</button>
      </div>
    </div>
  `;
}

function initCookieBanner() {
  const consent = window.localStorage.getItem(COOKIE_STORAGE_KEY);
  if (consent) {
    return;
  }

  const content = translations[currentLanguage] || translations.it;
  const banner = document.createElement("aside");
  banner.className = "cookie-banner";
  banner.setAttribute("role", "dialog");
  banner.setAttribute("aria-live", "polite");
  banner.innerHTML = getCookieBannerMarkup(content);
  document.body.classList.add("has-cookie-banner");
  document.body.appendChild(banner);

  banner.addEventListener("click", (event) => {
    const action = event.target.closest("[data-cookie-action]")?.dataset.cookieAction;
    if (!action) {
      return;
    }

    window.localStorage.setItem(COOKIE_STORAGE_KEY, action);
    document.body.classList.remove("has-cookie-banner");
    banner.remove();
  });
}

function toggleLanguageMenu(forceState) {
  if (!languageSwitcher || !languageTrigger) {
    return;
  }

  const nextState = typeof forceState === "boolean" ? forceState : !languageSwitcher.classList.contains("is-open");
  languageSwitcher.classList.toggle("is-open", nextState);
  languageTrigger.setAttribute("aria-expanded", String(nextState));
}

function toggleMenu(forceState) {
  if (!navToggle || !navPanel) {
    return;
  }

  const nextState = typeof forceState === "boolean" ? forceState : !navPanel.classList.contains("is-open");
  navPanel.classList.toggle("is-open", nextState);
  navToggle.classList.toggle("is-active", nextState);
  navToggle.setAttribute("aria-expanded", String(nextState));
  document.body.classList.toggle("menu-open", nextState);
}

function initMenu() {
  if (!navToggle || !navPanel) {
    return;
  }

  navToggle.addEventListener("click", () => toggleMenu());

  navLinks.forEach((link) => {
    link.addEventListener("click", () => toggleMenu(false));
  });

  window.addEventListener("resize", () => {
    if (window.innerWidth > COLLAPSED_NAV_BREAKPOINT) {
      toggleMenu(false);
    }
  });
}

function initLanguageSwitcher() {
  if (!languageSwitcher || !languageTrigger) {
    return;
  }

  languageTrigger.addEventListener("click", () => {
    toggleLanguageMenu();
  });

  langButtons.forEach((button) => {
    button.addEventListener("click", () => {
      applyTranslations(button.dataset.lang);
      toggleLanguageMenu(false);
    });
  });

  document.addEventListener("click", (event) => {
    if (!languageSwitcher.contains(event.target)) {
      toggleLanguageMenu(false);
    }
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") {
      toggleLanguageMenu(false);
    }
  });
}

async function preloadHeroFrames() {
  const loaders = heroFrames.map(
    (frame) =>
      new Promise((resolve) => {
        const image = new Image();
        image.decoding = "async";
        image.onload = async () => {
          try {
            await image.decode();
          } catch {
          }
          resolve(image);
        };
        image.onerror = () => resolve(null);
        image.src = frame;
      })
  );

  const frames = await Promise.all(loaders);
  loadedHeroFrames = frames.filter(Boolean);
}

function initHeroAnimation() {
  if (!heroCanvas) {
    return;
  }

  const context = heroCanvas.getContext("2d", { alpha: false });
  if (!context) {
    return;
  }

  let frameIndex = 0;
  let lastTimestamp = 0;
  const fps = 18;
  const frameDuration = 1000 / fps;

  function shouldContainHeroFrame() {
    return window.matchMedia("(max-width: 680px)").matches;
  }

  function resizeCanvas() {
    const ratio = window.devicePixelRatio || 1;
    const width = heroCanvas.clientWidth;
    const height = heroCanvas.clientHeight;
    heroCanvas.width = Math.round(width * ratio);
    heroCanvas.height = Math.round(height * ratio);
    context.setTransform(ratio, 0, 0, ratio, 0, 0);
    drawFrame(loadedHeroFrames[frameIndex] || loadedHeroFrames[0]);
  }

  function drawFrame(image) {
    if (!image) {
      return;
    }

    const canvasWidth = heroCanvas.clientWidth;
    const canvasHeight = heroCanvas.clientHeight;
    const imageRatio = image.width / image.height;
    const canvasRatio = canvasWidth / canvasHeight;
    const useContain = shouldContainHeroFrame();

    let drawWidth = canvasWidth;
    let drawHeight = canvasHeight;
    let offsetX = 0;
    let offsetY = 0;

    if (useContain) {
      if (imageRatio > canvasRatio) {
        drawWidth = canvasWidth;
        drawHeight = drawWidth / imageRatio;
        offsetY = (canvasHeight - drawHeight) / 2;
      } else {
        drawHeight = canvasHeight;
        drawWidth = drawHeight * imageRatio;
        offsetX = (canvasWidth - drawWidth) / 2;
      }
    } else {
      if (imageRatio > canvasRatio) {
        drawHeight = canvasHeight;
        drawWidth = drawHeight * imageRatio;
        offsetX = (canvasWidth - drawWidth) / 2;
      } else {
        drawWidth = canvasWidth;
        drawHeight = drawWidth / imageRatio;
        offsetY = (canvasHeight - drawHeight) / 2;
      }
    }

    context.fillStyle = "#050505";
    context.fillRect(0, 0, canvasWidth, canvasHeight);
    context.drawImage(image, offsetX, offsetY, drawWidth, drawHeight);
  }

  function animate(timestamp) {
    if (!loadedHeroFrames.length) {
      window.requestAnimationFrame(animate);
      return;
    }

    if (!lastTimestamp || timestamp - lastTimestamp >= frameDuration) {
      drawFrame(loadedHeroFrames[frameIndex]);
      frameIndex = (frameIndex + 1) % loadedHeroFrames.length;
      lastTimestamp = timestamp;
    }

    window.requestAnimationFrame(animate);
  }

  window.addEventListener("resize", resizeCanvas);
  resizeCanvas();
  window.requestAnimationFrame(animate);
}

function initRevealAnimations() {
  const revealElements = document.querySelectorAll(".reveal");

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    {
      threshold: 0.18,
      rootMargin: "0px 0px -30px 0px"
    }
  );

  revealElements.forEach((element) => observer.observe(element));
}

function initCarousel() {
  const carousel = document.querySelector("[data-carousel]");
  if (!carousel) {
    return;
  }

  const track = carousel.querySelector("[data-carousel-track]");
  const viewport = carousel.querySelector("[data-carousel-viewport]");
  const slides = Array.from(track.children);
  const prevButton = carousel.querySelector("[data-carousel-prev]");
  const nextButton = carousel.querySelector("[data-carousel-next]");
  const dotsContainer = document.querySelector("[data-carousel-dots]");

  let index = 0;
  let visibleSlides = getVisibleSlides();
  let maxIndex = Math.max(0, slides.length - visibleSlides);
  let touchStartX = 0;
  let touchDelta = 0;

  function getVisibleSlides() {
    if (window.innerWidth <= 680) {
      return 1;
    }
    if (window.innerWidth <= 1080) {
      return 2;
    }
    return 3;
  }

  function buildDots() {
    const dotCount = Math.max(1, slides.length - visibleSlides + 1);
    dotsContainer.innerHTML = "";
    Array.from({ length: dotCount }).forEach((_, dotIndex) => {
      const dot = document.createElement("button");
      dot.type = "button";
      dot.classList.toggle("active", dotIndex === index);
      dot.addEventListener("click", () => {
        index = dotIndex;
        updateCarousel();
      });
      dotsContainer.appendChild(dot);
    });
  }

  function updateCarousel() {
    visibleSlides = getVisibleSlides();
    maxIndex = Math.max(0, slides.length - visibleSlides);
    index = Math.min(index, maxIndex);
    const slideWidth = viewport.clientWidth / visibleSlides;
    track.style.transform = `translateX(-${index * slideWidth}px)`;

    slides.forEach((slide, slideIndex) => {
      slide.classList.toggle("is-active", slideIndex >= index && slideIndex < index + visibleSlides);
    });

    Array.from(dotsContainer.children).forEach((dot, dotIndex) => {
      dot.classList.toggle("active", dotIndex === index);
    });
  }

  function moveCarousel(direction) {
    index += direction;
    if (index < 0) {
      index = maxIndex;
    }
    if (index > maxIndex) {
      index = 0;
    }
    updateCarousel();
  }

  prevButton.addEventListener("click", () => moveCarousel(-1));
  nextButton.addEventListener("click", () => moveCarousel(1));

  viewport.addEventListener("pointerdown", (event) => {
    touchStartX = event.clientX;
    touchDelta = 0;
  });

  viewport.addEventListener("pointermove", (event) => {
    if (!touchStartX) {
      return;
    }
    touchDelta = event.clientX - touchStartX;
  });

  const handlePointerEnd = () => {
    if (Math.abs(touchDelta) > 40) {
      moveCarousel(touchDelta < 0 ? 1 : -1);
    }
    touchStartX = 0;
    touchDelta = 0;
  };

  viewport.addEventListener("pointerup", handlePointerEnd);
  viewport.addEventListener("pointerleave", handlePointerEnd);

  buildDots();
  updateCarousel();

  window.addEventListener("resize", () => {
    const previousVisibleSlides = visibleSlides;
    visibleSlides = getVisibleSlides();
    maxIndex = Math.max(0, slides.length - visibleSlides);
    if (visibleSlides !== previousVisibleSlides) {
      buildDots();
    }
    updateCarousel();
  });

  window.setInterval(() => {
    moveCarousel(1);
  }, 4200);
}

